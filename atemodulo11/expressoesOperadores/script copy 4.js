/*
    Operadores Aritimétricos
*/

// Multiplicação
    console.log("Multiplicação: " + 3.2 * 3)
// Divisão
    console.log("Divisão: " + 3.2 / 3)
// Soma
    console.log("Soma: " + 3.2 + 3)
// Subtração
    console.log("Subtração: " + (32 - 3))
// Resto da divisão % remainder
    let remainder
    remainder = 11 % 10
    console.log("Resto da divisão" + remainder)

// Incremento ++
    let increment = 0
    increment++
    console.log("Incremento: " + increment)
// Decremento --
    let decrement = 3
    decrement--
    console.log("Decremento: " + decrement)
// Exponencial **
    console.log("Exponencial: " + 3 ** 3)